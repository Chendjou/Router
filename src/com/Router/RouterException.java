package com.Router;

public class RouterException extends Exception{
	private static final long serialVersionUID = 6936623599429902741L;

	public RouterException() {
		super();
	}

	public RouterException(String message, Throwable cause, boolean arg2, boolean arg3) {
		super(message, cause, arg2, arg3);
	}

	public RouterException(String message, Throwable cause) {
		super(message, cause);
	}

	public RouterException(String message) {
		super(message);
	}

	public RouterException(Throwable cause) {
		super(cause);
	}
}	
