package com.urix.router.exception;

import com.urix.router.RouterException;

public class RouterConfigException extends RouterException {
	private static final long serialVersionUID = -4139377020906555029L;

	public RouterConfigException() {
		super();
	}


	public RouterConfigException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RouterConfigException(String arg0) {
		super(arg0);
	}

	public RouterConfigException(Throwable arg0) {
		super(arg0);
	}

}
